/* 
–° –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã –≤–≤–æ–¥–∏—Ç—Å—è —Å—Ç–æ–∏–º–æ—Å—Ç—å –ø—Ä–æ–¥—É–∫—Ç–∞ –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ä–µ–¥—Å—Ç–≤.
–ï—Å–ª–∏ –¥–µ–Ω–µ–≥ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç, —Ç–æ –æ—Ç–∫–∞–∑—ã–≤–∞–µ–º –≤ –ø–æ–∫—É–ø–∫–µ, –∏–Ω–∞—á–µ, –µ—Å–ª–∏ –µ—â–µ –æ—Å—Ç–∞—é—Ç—Å—è –¥–µ–Ω—å–≥–∏, —Ç–æ –ø—Ä–µ–¥–ª–∞–≥–∞–µ–º –∫—É–ø–∏—Ç—å –ª–æ—Ç–µ—Ä–µ—é –∑–∞ 4 –≥—Ä–Ω.
*/

// –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö
const productPrice = parseFloat(prompt(`–í–≤–µ–¥–∏—Ç–µ —Å—Ç–æ–∏–º–æ—Å—Ç—å –ø—Ä–æ–¥—É–∫—Ç–∞`, 10))
const userMoney = parseFloat(prompt(`–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–∞—à–∏—Ö —Å—Ä–µ–¥—Å—Ç–≤`, 20.10))

// –í—ã–≤–æ–¥
// –†–∞–∑–Ω–∏—Ü–∞ –≤ —Ü–µ–Ω–µ —Ç–æ–≤–∞—Ä–∞ –∏ —Å—Ä–µ–¥—Å—Ç–≤ –ø–æ–∫—É–ø–∞—Ç–µ–ª—è
const moneyDifference = userMoney - productPrice
// –¶–µ–Ω–∞ –ª–æ—Ç–µ—Ä–µ–∏
const priceOfLotteries = 4

if (moneyDifference >= 0) { // –ï—Å–ª–∏ —Ö–≤–∞—Ç–∞–µ—Ç –¥–µ–Ω–µ–≥ –Ω–∞ –ø–æ–∫—É–ø–∫—É (userMoney >= productPrice)
	document.write(`<p>–¢–æ–≤–∞—Ä —É—Å–ø–µ—à–Ω–æ –ø—Ä–∏–æ–±—Ä–µ—Ç–µ–Ω. –°–¥–∞—á–∞ ${moneyDifference.toFixed(2)} –≥—Ä–Ω.</p>`)

	if (moneyDifference >= priceOfLotteries) { // –ï—Å–ª–∏ –æ—Å—Ç–∞–ª–∏—Å—å —Å—Ä–µ–¥—Å—Ç–≤–∞ –Ω–∞ –ª–æ—Ç–æ
		// –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ª–æ—Ç–µ—Ä–µ–π, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–µ—Ç –ø–æ–∑–≤–∞–æ–ª–∏—Ç—å —Å–µ–±–µ –∫—É–ø–∏—Ç—å. –ì—É–ª—è—Ç—å, —Ç–∞–∫ –≥—É–ª—è—Ç—å üòâ
		const quantityOfLotteries = Math.floor(moneyDifference / priceOfLotteries)
		document.write(`
								<p>–ù–µ –∂–µ–ª–∞–µ—Ç–µ –ø—Ä–∏–æ–±—Ä–µ—Å—Ç–∏ –ª–æ—Ç–µ—Ä–µ—é –∑–∞ ${priceOfLotteries} –≥—Ä–Ω.? 
								–í—ã –º–æ–∂–µ—Ç–µ –∫—É–ø–∏—Ç—å ${quantityOfLotteries} —à—Ç.</p>
							`)
	}

} else // –ï—Å–ª–∏ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç –¥–µ–Ω–µ–≥
	document.write(`<p>–í–∞–º –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç ${Math.abs(moneyDifference)} –≥—Ä–Ω.</p>`)